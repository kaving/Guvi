public class PrintingNumber {
    public static void main(String[] args){
        for(int i=10;i<=50;i++){
            System.out.print(" " +i);
    }
    System.out.println("");
    int i= 10;
    while (i<=50){
        System.out.print(i + " ");
        i++;
    }
    }
}
